//Operadores Aritméticos

10 + 15 // Soma
10 - 15 // Subtração
10 * 15 // Multiplicação
15 / 10 // Divisão

15++ // Incremento de +1
15-- // Diminuição de -1

15 % 5 // Módulo, o resto da divisão de 15/5 = 0
15 % 2 // Módulo, o resto da divisão de 15/2 = 1 

// Operadores de Comparação

10 == 15 // Igualdade, false
10 != 15 // Desigualdade, true

10 === '10' // Igualdade Estrita, false
10 !== 15 // Desigualdade Estrita, true

15 > 15 // Maior que, false
15 >= 15 // Maior ou Igual que, true
10 < 15 // Menor que, true
10 <= 15 // Menor ou Igual que, true 


//Operadores Lógicos (sempre retornam Booleans)

// AND = &&
false && true == false;
true && true == true;

// OR = || 
false || true == true;
true || true == true;

// NOT (!)
!false // true
!(10 > 15) // true

//Operadores de Concatenação

var name = 'Paola';
var surname = 'Pagotto';
var fullName = name + " " + surname;

var rowNumber = 'M';
var seatNumber = 7;
var seatPlace = rowNumber + seatNumber;
 